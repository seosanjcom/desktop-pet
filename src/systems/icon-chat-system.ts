export function getIconMessage(
  icon: DesktopIcon,
  characterType: CharacterType = "orangutan"
): string {
  const category = icon.category || "other";
  const messages = ICON_MESSAGES[category]?.[characterType] || ICON_MESSAGES.other[characterType];
  return messages[Math.floor(Math.random() * messages.length)];
}

export function findNearbyIcon(
  petX: number,
  petY: number,
  icons: DesktopIcon[],
  range = 120
): DesktopIcon | null {
  let closest: DesktopIcon | null = null;
  let minDist = range;

  for (const icon of icons) {
    const dx = petX - icon.x;
    const dy = petY - icon.y;
    const dist = Math.sqrt(dx * dx + dy * dy);
    if (dist < minDist) {
      minDist = dist;
      closest = icon;
    }
  }
  return closest;
}

export function pickRandomIconTarget(icons: DesktopIcon[]): DesktopIcon | null {
  if (icons.length === 0) return null;
  return icons[Math.floor(Math.random() * icons.length)];
}
